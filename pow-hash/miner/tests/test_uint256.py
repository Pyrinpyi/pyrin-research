from uint256 import Uint256

def test_uint256():
    assert Uint256.from_compact_target_bits(0x1d00ffff).hex() == "00000000ffff0000000000000000000000000000000000000000000000000000"
    assert Uint256.from_compact_target_bits(520226630).hex() == "0002074600000000000000000000000000000000000000000000000000000000"